package com.tonydantona.chordtrainer;

public class Chord {
	
	private String Name;
	private String Filter;
	
	public void setName(String name) {
		Name = name;
	}
	
	public String getName() {
		return Name;
	}
	
	public void setFilter(String filter) {
		Filter = filter;
	}
	
	public String getFilter() {
		return Filter;
	}
}
